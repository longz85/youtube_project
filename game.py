def safe_slice_loop(colored_text, start, width):
    # Lặp đoạn văn bản đủ để luôn lấy được đoạn dài width
    visible_len = visible_length(colored_text)
    if visible_len == 0:
        return ' ' * width  # nếu dòng trống, trả về khoảng trắng

    repeat_times = (width // visible_len) + 3
    repeated = colored_text * repeat_times
    return safe_slice(repeated, start % visible_length(colored_text), width)

def safe_slice(colored_text, start, width):
    parts = ansi_pattern.split(colored_text)
    display = ''
    visible = 0
    active_code = ''
    i = 0

    while i < len(parts):
        if ansi_pattern.match(parts[i]):
            active_code = parts[i]
            i += 1
            continue

        segment = parts[i]
        for c in segment:
            if visible >= start + width:
                break
            if visible >= start:
                display += active_code + c
                active_code = ''
            visible += 1
        i += 1

        if visible >= start + width:
            break

    return display + '\033[0m'
    
